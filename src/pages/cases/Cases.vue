<template>
  <div class="case-container">
    <b-row>
      <b-col></b-col>
    </b-row>
    <Waterfall
      :align="align"
      :lineGap="200"
      :min-line-gap="100"
      :max-line-gap="220"
      :single-max-width="300"
      :watch="showCases"
      @reflowed="reflowed"
      ref="waterfall">

      <WaterfallSlot
        v-for="(item, index) in showCases"
        :width="item.width"
        :height="item.height"
        :order="index"
        :key="index"
        move-class="item-move">

        <CaseCard class="case-card" v-bind="item"/>

      </WaterfallSlot>

    </Waterfall>
    <div v-show="isBusy" class="loader">Loading...</div>

    <button @click="showCaseType = 'tiyan'"></button>
  </div>
</template>

<script>
  import CaseCard from './CaseCard'
  import Waterfall from 'vue-waterfall/lib/waterfall'
  import WaterfallSlot from 'vue-waterfall/lib/waterfall-slot'

  export default {
    data () {
      return {
        isBusy: false,
        align: 'center',
        cases: [],
        showCaseType: 'all'
      }
    },

    computed: {
      showCases () {
        switch (this.showCaseType) {
          case 'all':
            return this.cases
          case 'tiyan':
            return this.cases.filter(item => item.type = 'tiyan')
        }
      }
    },

    mounted () {
      const vm = this

      vm.getCases()
      window.addEventListener('scroll', function () {
        const scrollTop = document.documentElement.scrollTop || document.body.scrollTop
        if (scrollTop + window.innerHeight >= document.body.clientHeight) {
          vm.getCases()
        }
      })
    },

    methods: {
      reflowed: function () {
        this.isBusy = false
      },

      getCases () {
        if (this.isBusy) return

        this.isBusy = true
        this.axios.get('/api/getCases')
          .then(res => {
            this.cases.push(...res.data.cases)
          })
      }
    },

    components: {Waterfall, WaterfallSlot, CaseCard}
  }
</script>

